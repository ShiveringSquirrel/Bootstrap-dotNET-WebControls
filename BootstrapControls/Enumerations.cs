
namespace BootstrapControls
{
    /// <summary>
    /// This class holds all the general enumerations
    /// </summary>
    public class Enumerations
    {
        /// <summary>
        /// Describes the state of a control
        /// </summary>
        public enum States
        {
            Normal, Disabled, Static
        }

        /// <summary>
        /// Describes the style of a form-group bootstrap element
        /// </summary>
        public enum FormGroupStyle
        {
            Normal, Success, Warning, Error
        }

        /// <summary>
        /// Describes the style of an alert bootstrap element
        /// </summary>
        public enum AlertStyle
        {
            Info, Success, Warning, Error
        }

        /// <summary>
        /// Describes the style of a button bootstrap element
        /// </summary>
        public enum ButtonStyle
        {
            Default, Primary, Info, Success, Warning, Danger, Link
        }
    }
}
